// Створіть змінну age і присвойте їй випадкове значення від 10 до 30 (можете вручну або Math.random()).
// Створіть булеву змінну isAdult, яка визначає, чи є особа повнолітньою (age >= 18).
// Виведіть значення isAdult



// Генеруємо випадковий вік від 10 до 30
const age = Math.floor(Math.random() * (30 - 10 + 1)) + 10;

// Визначаємо, чи є особа повнолітньою
const isAdult = age >= 18;

// Виводимо результати
console.log(`Вік особи: ${age}`);
console.log(`Чи є особа повнолітньою: ${isAdult}`);
